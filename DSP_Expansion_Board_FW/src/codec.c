bool initCODEC(void)
{
	int8_t state = 0;

	bool errorFlag = false;

	state += I2Cwrite(0x30, AIC3204_SP0 , 2);
	state += I2Cwrite(0x30, AIC3204_RST , 2);
	state += I2Cwrite(0x30, AIC3204_SP1 , 2);
	state += I2Cwrite(0x30, AIC3204_AVDD_OFF , 2);
	state += I2Cwrite(0x30, AIC3204_ANA_ON , 2);

	if(state != 0) {errorFlag = true;}

	return errorFlag;
}

